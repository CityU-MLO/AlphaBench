"""
Run T2 Ranking Evaluation

Asks an LLM to identify the top-K factors from a set of N, then measures
Precision@K and NDCG@K against the ground-truth rankings.

Expected data layout (built by build_base_eval_datasets.py):
  benchmark/data/evaluate/built/ranking/
    all_env_scenarios.json      ← combined ranking test cases
    ranking_<regime>.json       ← per-regime files (informational)

  benchmark/data/evaluate/pool/
    factor_pool.json            ← {factor_name: expression}

Output written to:
  runs/T2/<model>_<cot>/
    ranking_results.json        ← raw LLM outputs
    report/                     ← generated by generate_report()
      ranking_summary.csv
      report.md

Usage:
  # Default settings (GPT-4.1, no CoT)
  python example/02_run_evaluation/run_t2_ranking.py

  # With CoT, different model
  python example/02_run_evaluation/run_t2_ranking.py \
      --model deepseek-chat --cot

  # Local vLLM server
  python example/02_run_evaluation/run_t2_ranking.py \
      --model Qwen2.5-72B-Instruct --local --local_port 8000

  # Only run evaluation, skip LLM inference (results already exist)
  python example/02_run_evaluation/run_t2_ranking.py --eval_only
"""

from __future__ import annotations

import argparse
import os
import sys

_REPO_ROOT = os.path.abspath(os.path.join(os.path.dirname(__file__), "..", ".."))
if _REPO_ROOT not in sys.path:
    sys.path.insert(0, _REPO_ROOT)

from benchmark.engine.evaluate import (
    benchmark_ranking_performance,
    generate_report,
)


_DATA_DIR     = "benchmark/data/evaluate/built/ranking"
_POOL_DIR     = "benchmark/data/evaluate/pool"
_OUTPUT_ROOT  = "runs/T2"


def _parse_args():
    p = argparse.ArgumentParser(
        description="Run T2 ranking evaluation with an LLM.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    p.add_argument("--data_dir",   default=_DATA_DIR,
                   help=f"Directory containing all_env_scenarios.json. Default: {_DATA_DIR}")
    p.add_argument("--pool_dir",   default=_POOL_DIR,
                   help=f"Directory containing factor_pool.json. Default: {_POOL_DIR}")
    p.add_argument("--output_root", default=_OUTPUT_ROOT,
                   help=f"Root output directory. Default: {_OUTPUT_ROOT}")
    p.add_argument("--model",      default="gpt-4.1",
                   help="LLM model name (default: gpt-4.1)")
    p.add_argument("--cot",        action="store_true",
                   help="Enable chain-of-thought prompting.")
    p.add_argument("--local",      action="store_true",
                   help="Use a local vLLM server.")
    p.add_argument("--local_port", type=int, default=8000,
                   help="Port of local LLM server (default: 8000).")
    p.add_argument("--num_workers", type=int, default=8,
                   help="Parallel workers for batch LLM calls (default: 8).")
    p.add_argument("--eval_only",  action="store_true",
                   help="Skip LLM inference; only re-compute metrics from existing results.")
    return p.parse_args()


def main():
    args = _parse_args()

    # ---- Build paths ----
    run_dir = os.path.join(args.output_root, f"{args.model}_{args.cot}")
    os.makedirs(run_dir, exist_ok=True)

    # benchmark_ranking_performance expects a `data_path` that contains both
    # all_env_scenarios.json AND factor_pool.json at its root.
    # We construct a combined data_path directory via a thin shim.
    # Since our layout separates them, we pass both explicitly below.
    ranking_cases_path = os.path.join(args.data_dir, "all_env_scenarios.json")
    factor_pool_path   = os.path.join(args.pool_dir,  "factor_pool.json")

    for label, path in [
        ("Ranking test cases", ranking_cases_path),
        ("Factor pool",        factor_pool_path),
    ]:
        if not os.path.exists(path):
            print(f"[ERROR] {label} not found: {path}")
            print("  → Run build_base_eval_datasets.py first.")
            sys.exit(1)

    print(f"[T2 Ranking] model={args.model}  cot={args.cot}")
    print(f"  data:   {ranking_cases_path}")
    print(f"  output: {run_dir}")

    # ---- LLM Inference ----
    if not args.eval_only:
        # benchmark_ranking_performance reads factor_pool from data_path/factor_pool.json
        # and scenarios from data_path/all_env_scenarios.json.
        # We copy/symlink factor_pool into the data_dir if not already there.
        _ensure_pool_in_data_dir(factor_pool_path, args.data_dir)

        benchmark_ranking_performance(
            data_path=args.data_dir,
            output_path=run_dir,
            enable_CoT=args.cot,
            model=args.model,
            local=args.local,
            local_port=args.local_port,
            num_workers=args.num_workers,
        )
        print(f"[Done] ranking_results.json → {run_dir}")
    else:
        existing = os.path.join(run_dir, "ranking_results.json")
        if not os.path.exists(existing):
            print(f"[ERROR] --eval_only set but no results found at: {existing}")
            sys.exit(1)
        print(f"[eval_only] Using existing results: {existing}")

    # ---- Generate report ----
    print("\n[Report] Generating performance report...")
    generate_report(
        run_dir=run_dir,
        ranking_cases_path=ranking_cases_path,
        output_dir=os.path.join(run_dir, "report"),
        verbose=True,
    )


def _ensure_pool_in_data_dir(pool_path: str, data_dir: str) -> None:
    """
    benchmark_ranking_performance looks for factor_pool.json inside data_path.
    If the pool is in a separate directory, copy it over.
    """
    target = os.path.join(data_dir, "factor_pool.json")
    if os.path.exists(target):
        return
    import shutil
    shutil.copy2(pool_path, target)
    print(f"[Setup] Copied factor_pool.json to: {target}")


if __name__ == "__main__":
    main()
